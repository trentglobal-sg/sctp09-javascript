// REPETITION
// Doing something over and over again

// A while loop allows us to repeat lines of code in a block
let count = 0;
while (count < 10 ) {
    console.log("I will remember to bring my homework");
    count = count + 1;
}

// Show the numbers from 1 to 5
let num = 1;
while (num < 5) {
    console.log(num);
    num = num + 1;
}
console.log("Finished");

